pub type State = [u8; 16];

pub unsafe fn aes_encrypt(data: State, keys: State) -> State {
    let mut state = data;
    sub_bytes(&mut state);
    shift_rows(&mut state);
    mix_columns(&mut state);
    add_round_key(&mut state, &keys);
    return state;
}

fn mix_columns(state: &mut [u8; 16]) {
    for i in 0..4 {
        // Calculate indices for 1D array
        let idx0 = i;
        let idx1 = 4 + i;
        let idx2 = 8 + i;
        let idx3 = 12 + i;

        let t = state[idx0];
        let tmp = state[idx0] ^ state[idx1] ^ state[idx2] ^ state[idx3];

        let mut tm = state[idx0] ^ state[idx1];
        tm = mul(tm, 2);
        state[idx0] ^= tm ^ tmp;

        tm = state[idx1] ^ state[idx2];
        tm = mul(tm, 2);
        state[idx1] ^= tm ^ tmp;

        tm = state[idx2] ^ state[idx3];
        tm = mul(tm, 2);
        state[idx2] ^= tm ^ tmp;

        tm = state[idx3] ^ t;
        tm = mul(tm, 2);
        state[idx3] ^= tm ^ tmp;
    }
}

fn mul(a: u8, b: u8) -> u8 {
    if a != 0 && b != 0 {
        let log_a = LOG_TABLE[a as usize] as usize;
        let log_b = LOG_TABLE[b as usize] as usize;
        let log_sum = (log_a + log_b) % 255; // Modulo 255 to keep within bounds
        ALOG_TABLE[log_sum]
    } else {
        0
    }
}

fn shift_rows(state: &mut [u8; 16]) {
    // Rotate the second row 1 column to the left
    let temp = state[4];
    state[4] = state[5];
    state[5] = state[6];
    state[6] = state[7];
    state[7] = temp;

    // Rotate the third row 2 columns to the left
    let temp = state[8];
    state[8] = state[10];
    state[10] = temp;

    let temp = state[9];
    state[9] = state[11];
    state[11] = temp;

    // Rotate the fourth row 3 columns to the left
    let temp = state[12];
    state[12] = state[15];
    state[15] = state[14];
    state[14] = state[13];
    state[13] = temp;
}

fn add_round_key(state: &mut [u8; 16], key: &[u8; 16]) {
    for i in 0..16 {
        state[i] ^= key[i];
    }
}

fn sub_bytes(state: &mut [u8; 16]) {
    for i in 0..16 {
        state[i] = S_BOX[state[i] as usize];
    }
}